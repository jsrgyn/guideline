

for (let C = 0; C <= 10; C++) {
  console.log(`O valor de C é: ${C}`);
}

console.log('-----------------------');

// Passo de 2 em 2

for (let C = 0; C <= 10; C += 2) {
  console.log(`O valor de C é: ${C}`);
}

console.log('-----------------------');


// Decrementando de 10 até 1
for (let C = 10; C >= 1; C--) {
  console.log(`O valor de C é: ${C}`);
}

console.log('-----------------------');

// Decrementando de 2 em 2 sendo 10 até 0
for (let C = 10; C >= 0; C -= 2) {
  console.log(`O valor de C é: ${C}`);
}
console.log('-----------------------');

console.log('');
